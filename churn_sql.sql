select Gender,count(Gender) as TotalCount,
count(gender)*100.0 /(select count(*) from stg_churn) as percentage  --percentage distributution
from stg_churn group by Gender;

select Contract,count(Contract) as TotalCount,
count(Contract)*100.0 /(select count(*) from stg_churn) as percentage
from stg_churn group by Contract;

-- Out of all customers, how many are still active, how many have left (churned), and 
-- how many are newly joined — both in count and percentage terms

select Customer_Status,count(Customer_Status) as TotalCount,
count(Customer_Status)*100.0 /(select count(*) from stg_churn) as percentage
from stg_churn group by Customer_Status;


--“How much revenue came from each customer group, how many customers were there in each, and 
--what percent of total revenue each group contributed?”

select Customer_Status,count(customer_status) as TotalCount,sum(Total_Revenue) as TotalRev,
-- Out of 100% of total revenue, how much came from customers in this Customer_Status group
sum(Total_Revenue)*100.0 /(select sum(Total_Revenue) from stg_churn) as revpercentage 
from stg_churn group by Customer_Status;

-- to know which state has higher contribution in our entire data
select State,count(State) as totalCount,
count(State)*100.0/(select count(*) from stg_churn) as statePer
from stg_churn group by State order by statePer desc;

select distinct internet_type from stg_churn;


